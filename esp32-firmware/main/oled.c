static uint8_t s_cursor_col = 0;
static uint8_t s_cursor_row = 0;

// ---- I2C helpers ----

static void oled_send_cmd(uint8_t cmd)
{
    uint8_t buf[2] = {0x00, cmd}; // 0x00 = command prefix
    i2c_master_transmit(s_dev, buf, 2, 10);
}

static void oled_send_data(const uint8_t *data, size_t len)
{
    // Use a small static buffer; split long transfers
    static uint8_t buf[129]; // 1 + 128 max
    while (len > 0) {
        size_t chunk = (len > 128) ? 128 : len;
        buf[0] = 0x40; // data prefix
        memcpy(buf + 1, data, chunk);
        i2c_master_transmit(s_dev, buf, chunk + 1, 10);
        data += chunk;
        len -= chunk;
    }
}

// ---- Public API ----

void oled_init(i2c_master_bus_handle_t bus)
{
    i2c_device_config_t cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = OLED_I2C_ADDR,
        .scl_speed_hz = 400000,
    };
    i2c_master_bus_add_device(bus, &cfg, &s_dev);

    // Init sequence for SSD1306 128x64
    oled_send_cmd(0xAE); // display off

    oled_send_cmd(0xD5); // clock div
    oled_send_cmd(0x80);
    oled_send_cmd(0xA8); // multiplex
    oled_send_cmd(0x3F); // 64-1
    oled_send_cmd(0xD3); // display offset
    oled_send_cmd(0x00);
    oled_send_cmd(0x40); // start line 0
    oled_send_cmd(0x8D); // charge pump
    oled_send_cmd(0x14); // enable
    oled_send_cmd(0x20); // memory mode: horizontal
    oled_send_cmd(0x00);
    oled_send_cmd(0xA1); // segment remap (col 127 = SEG0)
    oled_send_cmd(0xC8); // COM scan direction (COM[N-1] to COM0)
    oled_send_cmd(0xDA); // COM pins
    oled_send_cmd(0x12);
    oled_send_cmd(0x81); // contrast
    oled_send_cmd(0xCF);
    oled_send_cmd(0xD9); // precharge
    oled_send_cmd(0xF1);
    oled_send_cmd(0xDB); // VCOM detect
    oled_send_cmd(0x40);
    oled_send_cmd(0xA4); // resume to RAM content
    oled_send_cmd(0xA6); // normal (not inverted)

    oled_send_cmd(0xAF); // display on

    oled_clear();
    oled_flush();

    ESP_LOGI(TAG_OLED, "SSD1306 initialized (addr=0x%02X)", OLED_I2C_ADDR);
}

void oled_clear(void)
{
    memset(s_fb, 0, sizeof(s_fb));
    s_cursor_col = 0;
    s_cursor_row = 0;
}

void oled_flush(void)
{
    for (uint8_t page = 0; page < OLED_PAGES; page++) {
        oled_send_cmd(0xB0 | page);  // set page
        oled_send_cmd(0x00);         // col low = 0
        oled_send_cmd(0x10);         // col high = 0
        oled_send_data(s_fb[page], OLED_WIDTH);
    }
}

void oled_set_cursor(uint8_t col, uint8_t row)
{
    s_cursor_col = (col < OLED_COLS) ? col : OLED_COLS - 1;
    s_cursor_row = (row < OLED_ROWS) ? row : OLED_ROWS - 1;
}

void oled_write_char(char c)
{
    if (c < 0x20 || c > 0x7E) return;
    uint8_t idx = c - 0x20;
    uint8_t px = s_cursor_col * 6;
    uint8_t py = s_cursor_row;

    if (px + 6 > OLED_WIDTH) return;

    for (int col = 0; col < 6; col++) {
        uint8_t bits = FONT6X8[idx][col];
        s_fb[py][px + col] = bits;
    }
    s_cursor_col++;
    if (s_cursor_col >= OLED_COLS) {
        s_cursor_col = 0;
        s_cursor_row = (s_cursor_row + 1) % OLED_ROWS;
    }
}

void oled_write_str(const char *s)
{
    while (*s) {
        oled_write_char(*s++);
    }
}

void oled_write_line(uint8_t row, const char *s)
{
    oled_set_cursor(0, row);
    // Clear the row first
    for (int c = 0; c < OLED_COLS; c++) {
        uint8_t px = c * 6;
        for (int i = 0; i < 6; i++) {
            if (px + i < OLED_WIDTH) s_fb[row][px + i] = 0;
        }
    }
    oled_write_str(s);
}

void oled_fill_rect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, bool on)
{
    if (x >= OLED_WIDTH || y >= OLED_HEIGHT) return;
    if (x + w > OLED_WIDTH) w = OLED_WIDTH - x;
    if (y + h > OLED_HEIGHT) h = OLED_HEIGHT - y;

    uint8_t page_start = y / 8;
    uint8_t page_end = (y + h - 1) / 8;
    uint8_t bit_start = y % 8;
    uint8_t bit_end = (y + h - 1) % 8;

    for (uint8_t page = page_start; page <= page_end; page++) {
        uint8_t mask = 0xFF;
        if (page == page_start && bit_start > 0) mask <<= bit_start;
        if (page == page_end) mask &= (0xFF >> (7 - bit_end));

        for (uint8_t col = x; col < x + w; col++) {
            if (on) s_fb[page][col] |= mask;
            else    s_fb[page][col] &= ~mask;
        }
    }
}
